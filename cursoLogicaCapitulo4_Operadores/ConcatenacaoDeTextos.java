package cursoLogicaCapitulo4_Operadores;

public class ConcatenacaoDeTextos {
    public static void main(String[] args) {
        String inicio = "Esse ";
        String meio = "é um exemplo ";
        String fim = "de concatenação de textos ";

        Integer numeroExUm = 1;
        Integer numeroExDois = 2;

        System.out.println(inicio + meio + fim);
        System.out.println(numeroExUm + " " + numeroExDois);
    }
}
